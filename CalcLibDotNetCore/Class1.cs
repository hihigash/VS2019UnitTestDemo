using System;

namespace CalcLibDotNetCore
{
    public static class Calc
    {
        public static int Add(int x1, int x2)
        {
            return x1 + x2;
        }

        public static int Sub(int x1, int x2)
        {
            return x1 - x2;
        }

        public static int Mul(int x1, int x2)
        {
            return x1 * x2;
        }

        public static int Div(int x1, int x2)
        {
            return x1 / x2;
        }
    }
}
